-- lua5.3的重要更新之一是引入整型
-- 形如整型的字面量被解释为整形，不过类型仍然是number
4 --> 4
0.4 --> 0.4
4.57e-3 --> 0.00457
0.3e12 --> 3000000000000.0
5E+20 --> 5e+20

type(3) --> number
type(3.5) --> number

-- 整型和浮点数可以隐式转换
-- 相等性定义为值的相等性
1 == 1.0 --> true
-3 == -3.0 --> true
0.2e3 == 200 --> true

-- math.type函数可以区别出整型和浮点数
math.type(3) --> integer
math.type(3.0) --> float

-- 大部分语言都有的十六进制常量
0xff --> 255
0x1A3 --> 419
0x0.2 --> 0.125

-- 与大部分语言不同之处在于
-- 有十六进制浮点型常量
-- p或P后是指数部分
0x1p-1 --> 0.5
0xa.bp2 --> 42.75