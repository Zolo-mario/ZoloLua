-- 两个整型值的加减乘和取负返回整型，否则是浮点型
13+15 --> 28
13.0+15.0 --> 28.0

-- 一个整型一个浮点型则整型先被转换成浮点型
13.0+25 --> 38.0
-(3*6.0) --> -18.0

-- 两个整型相除总是返回浮点型
3.0/2.0 --> 1.5
3/2 --> 1.5

-- floor除法运算符
-- 整型返回整型，浮点型返回浮点型
3//2 --> 1
3.0//2 --> 1.0
6//2 --> 3
6.0//2.0 --> 3.0
-9//2 --> -5
1.5//0.5 --> 3.0

